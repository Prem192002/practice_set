import java.util.Scanner;
public class loops {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        System.out.println("Enter the number");
        int num = sc.nextInt();

        
        for (int n=num;n>=1;n=n-1) {
            System.out.println(n);
            
        }
        
        

    }
    
}
